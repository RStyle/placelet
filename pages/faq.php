				<p>Hier findest du einige häufig gestellte Fragen. Wenn dir das nicht weiterhilft, <a href="kontakt">kontaktiere uns</a>.</p>
				<p class="questions pseudo_link" id="question_1"><span class="question_arrow1 arrow_right"></span>&nbsp;Wie kann ich unangemessene Bilder oder Kommentare löschen beziehungsweise melden?</p>
				<hr style="margin-top: 0; height: 3px; background-color: #ddd; border: none;">
				<div class="answers" id="answer_1">
					<p>
						Wenn du auf unserer Seite unangemessene oder anstößige Bilder oder Kommentare (zum Beispiel Spam) findest, solltest du diese der Seitenadministration melden, damit sie gelöscht werden.
						Klicke dazu auf den Button mit dem "X" rechts oben neben dem Bild oder Kommentar.<br>
						Als Inhaber eines Armbandes hast du zudem immer die Möglichkeit, Bilder oder Kommentare über denselben Button direkt zu löschen.
					</p>
				</div>
				
				<p class="questions pseudo_link" id="question_2"><span class="question_arrow2 arrow_right"></span>&nbsp;Warum sollte ich mich auf dieser Seite registrieren?</p>
				<hr style="margin-top: 0; height: 3px; background-color: #ddd; border: none;">
				<div class="answers" id="answer_2">
					<p>
						Auch wenn du auch ohne Account bestimmte Funktionen unserer Webseite nutzen kannst, bringt dir ein eigener Account einige Vorteile.
						Er ist zum Einen erforderlich, um selbst ein Armband um die Welt schicken zu können. Du erhältst außerdem eine eigene Profilseite, anhand derer deine Freunde deine Armbänder und Uploads nachverfolgen können und du wirst diesen unschönen Gast-Tag vor allen deinen Kommentaren los.
					</p>
				</div>
				
				<p class="questions pseudo_link" id="question_3"><span class="question_arrow3 arrow_right"></span>&nbsp;Wie können meine Freunde den Weg meines Armbandes verfolgen?</p>
				<hr style="margin-top: 0; height: 3px; background-color: #ddd; border: none;">
				<div class="answers" id="answer_3">
					<p>
						Generell kannst Du dein Armband überall mithilfe eines permanenten Links verbreiten ("www.placelet.de/[Dein Armbandname]"). Zudem kann das Armband über die Suchleiste mit dem Armbandnamen, den du selbst festlegen kannst, gefunden werden.
						Registrierte Benutzer haben auch die Möglichkeit, dein Armband zu abonnieren und so automatisch über neue Bilder zu diesem Armband informiert zu werden.
					</p>
				</div>
				
				<p class="questions pseudo_link" id="question_4"><span class="question_arrow4 arrow_right"></span>&nbsp;Was kann ich tun, wenn ich die ID eines meiner Armbänder vergessen habe?</p>
